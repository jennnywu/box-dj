#ifndef LEDS_H
#define LEDS_H

/*------------------------------------------------------------------------------------------------*/
// HEADERS                                                                                        */
/*------------------------------------------------------------------------------------------------*/

#include "esp_err.h"

/*------------------------------------------------------------------------------------------------*/
// MACROS                                                                                         */
/*------------------------------------------------------------------------------------------------*/

#define NUM_LEDS    3    // Number of LEDs in the scroll sequence

/*------------------------------------------------------------------------------------------------*/
// FUNCTION DECLARATIONS                                                                          */
/*------------------------------------------------------------------------------------------------*/

/**************************************************************************************************/
/**
 * @brief Initialize all LED GPIOs as outputs (active-low sinks)
 * @return esp_err_t ESP_OK on success
 */
/**************************************************************************************************/
esp_err_t leds_init(void);

/**************************************************************************************************/
/**
 * @brief Scroll to the next LED in sequence (turns off current, turns on next)
 */
/**************************************************************************************************/
void leds_scroll(void);

/**************************************************************************************************/
/**
 * @brief Turn on a specific LED (0-2)
 * @param led_index LED index (0, 1, or 2)
 */
/**************************************************************************************************/
void leds_set(uint8_t led_index);

/**************************************************************************************************/
/**
 * @brief Turn off all LEDs
 */
/**************************************************************************************************/
void leds_all_off(void);

#endif // LEDS_H
