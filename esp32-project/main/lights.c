typedef enum {
    LIGHT_OFF = 0,
    LIGHT_ONE = 1,
    LIGHT_TWO = 2,
    LIGHT_THREE = 3
} light_color_t;

static light_color_t current_light = LIGHT_OFF;

/*------------------------------------------------------------------------------------------------*/
/* FUNCTION PROTOTYPES                                                                            */
/*------------------------------------------------------------------------------------------------*/



/*------------------------------------------------------------------------------------------------*/
/* FUNCTION DEFINITIONS                                                                           */
/*------------------------------------------------------------------------------------------------*/

esp_err_t scroll_lights(void)
{
    esp_err_t ret;

    switch (current_light) {
        case LIGHT_OFF:
            current_light = LIGHT_ONE;
            break;
        case LIGHT_ONE:
            current_light = LIGHT_TWO;
            break;
        case LIGHT_TWO:
            current_light = LIGHT_THREE;
            break;
        case LIGHT_THREE:
            current_light = LIGHT_ONE;
            break;
    }



    return ESP_OK;
}